def few_shot(verdict_text: str) -> str:
    return f"""請分析台灣法院判決書中的外遇認定情形。以下是兩個分析範例：

---
範例一

判決書摘錄：
「原告主張被告與訴外人張○○於民國110年3月間，在台北市某旅館共宿一夜，有監視器畫面為證，被告否認但無法合理解釋。法院認定被告確與第三人有不正常男女關係。」

分析結果：
- 外遇認定：是
- 主要事證：監視器畫面顯示共宿
- 被告抗辯：否認但未提出反證
- 法院判斷：採信原告主張

---
範例二

判決書摘錄：
「原告雖主張被告與訴外人有曖昧訊息往來，然僅憑通訊軟體對話紀錄，難以認定已達通姦或破壞婚姻忠誠義務之程度，原告之訴駁回。」

分析結果：
- 外遇認定：否
- 主要事證：通訊軟體對話
- 被告抗辯：否認實質外遇行為
- 法院判斷：證據不足，不構成外遇

---
現在請用相同格式分析以下判決書：

判決書內容：
{verdict_text}

分析結果：
"""


def chain_of_thought(verdict_text: str) -> str:
    return f"""請仔細閱讀以下台灣法院判決書，並按照步驟逐步分析外遇認定情形。

判決書內容：
{verdict_text}

請按以下步驟思考：

步驟一：確認案件類型
- 這是民事還是刑事案件？
- 涉及哪些法條（如民法第184條、第195條，或刑法第239條）？

步驟二：整理雙方主張
- 原告（受害方）主張什麼？提出了哪些證據？
- 被告如何抗辯？理由是否合理？

步驟三：評估關鍵事證
- 列出判決書中提到的每一項事證
- 逐一判斷該事證的證明力強弱

步驟四：還原法院推理
- 法院最終採信哪一方的說法？
- 法院的推理邏輯是什麼？

步驟五：得出結論
- 本案是否認定外遇成立？
- 判決結果為何（賠償金額、離婚與否等）？

請完成上述每個步驟，最後給出整體分析摘要。
"""


