"""
5 種 prompt 策略 + 1 種證據萃取，用於分析台灣法院外遇判決書
"""

SYSTEM_PROMPT = "你是一位專精台灣家事法的法律分析助理，擅長解讀法院判決書。"


def plain_language(verdict_text: str) -> str:
    return f"""請用最簡單的白話文分析以下判決書，假設讀者完全不懂法律、也不懂法院文件。
絕對不能使用任何法律術語，寫得像在跟朋友說故事一樣。
請嚴格依照以下格式輸出，每個欄位簡短有力：

【一句話結論】
（外遇有沒有成立？判了什麼結果？）

【發生了什麼事】
（用說故事的方式描述案情，兩到三句話）

【對方被抓到什麼把柄】
（用條列式，每條一句白話，例如「兩人在旅館開房間8次，有登記紀錄」）

【對方怎麼辯解，法官信不信】
（被告說什麼藉口、法官為什麼信或不信）

【最後結果】
（誰贏誰輸，要賠多少錢，或其他結果）

【證據強度】
（用 ★ 評分，1到5顆星，並用一句話說明原因。例如：★★★★☆ 物證充分但缺乏直接性行為證明）

判決書內容：
{verdict_text}
"""


def evidence_extraction(verdict_text: str) -> str:
    """專門用來萃取結構化資料，不列入策略比較"""
    return f"""請分析以下判決書，以嚴格格式輸出下列資訊，不要加任何額外說明或換行以外的文字。

外遇認定: [是/否/不明]
損害賠償金額: [純數字，單位新台幣，無則填0]
證據類型: [從以下選出有出現的，以逗號分隔，沒有則填無: 旅館住宿紀錄, 通訊軟體對話, 監視器畫面, 目擊證人, 照片或影片, GPS位置資料, 信用卡消費紀錄, 私家偵探報告, 社群媒體貼文]

判決書內容：
{verdict_text}
"""


def zero_shot(verdict_text: str) -> str:
    return f"""請分析以下台灣法院判決書，判斷本案是否構成外遇（通姦或不貞行為），並說明理由。

判決書內容：
{verdict_text}
"""


